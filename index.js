for (const lang in words) {
  words[lang] = words[lang].sort((a, b) => a.length - b.length);
}

const rollRec = (options) => {
  const { lang, minLength, maxLength } = options;
  const randomElement = (array) =>
    array[Math.floor(Math.random() * array.length)];
  const result = randomElement(words[lang]);

  const invalidLenght = result.length < minLength || result.length > maxLength;

  return invalidLenght ? rollRec(options) : result;
};

const putDefaultOptionValues = (optionsParam) => {
  const options = { ...optionsParam } || {
    minLength: -Infinity,
    maxLength: +Infinity,
    lang: "en-US",
  };

  options.minLength = options.minLength || -Infinity;
  options.maxLength = options.maxLength || +Infinity;
  options.lang = options.lang || "en-US";

  return options;
};

const validateOptions = (options) => {
  const { lang, minLength, maxLength } = options;

  if (minLength > words[lang][words[lang].length - 1].length) {
    throw new Error(`Invalid min length ${minLength}`);
  }

  if (maxLength < words[lang][0].length) {
    throw new Error("Invalid max length");
  }
};

/**
 * Generates a random word with the options provided
 *
 * @param {
 *   minLength - minimum length of the word
 *   maxLength - maximum length of the word
 *   lang - ISO code of the words language
 * } options
 *
 */
const roll = (optionsParam) => {
  const options = putDefaultOptionValues(optionsParam);

  validateOptions(options);

  return rollRec(options);
};

const runTests = () => {
  const testForOptions = (options) => {
    let n = 100;

    while (n--) {
      const result = roll(options);

      if (result.length < options.minLength) {
        console.error(
          `\n\nTest error: ${JSON.stringify(options)}, ${result}\n\n`
        );
        process.exit(1);
      }

      if (result.length > options.maxLength) {
        console.error(
          `\n\nTest error: ${JSON.stringify(options)}, ${result}\n\n`
        );
        process.exit(1);
      }

      console.log(`Test passed: ${JSON.stringify(options)}, ${result}`);
    }
  };

  const testForLang = (lang) => {
    testForOptions({});

    testForOptions({ minLength: 3, lang });
    testForOptions({ minLength: 4, lang });
    testForOptions({ minLength: 8, lang });

    testForOptions({ maxLength: 6, lang });
    testForOptions({ maxLength: 7, lang });
    testForOptions({ maxLength: 8, lang });

    testForOptions({ minLength: 3, maxLength: 6, lang });
    testForOptions({ minLength: 4, maxLength: 7, lang });
    testForOptions({ minLength: 5, maxLength: 8, lang });
  };

  testForLang("en-US");
  testForLang("pt-PT");
  testForLang("pt-BR");
  testForLang("pl-PL");
  testForLang("it-IT");
};

if (process?.argv[2] === "__TEST_WORD_DICE__") {
  const a = Date.now();
  runTests();
  const b = Date.now();
  console.log(`Tests run in ${b - a} seconds`);
}

